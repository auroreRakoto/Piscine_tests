int	add(int a, int b)
{
	return (a + b);
}

int	sub(int a, int b)
{
	return (a - b);
}

int	mul(int a, int b)
{
	return (a * b);
}

int	divi(int a, int b)
{
	return (a / b);
}

int	modi(int a, int b)
{
	return (a % b);
}
