int	ft_iterative_power(int nb, int power)
{
	int	res;
	int	i;

	res = 1;
	if (power < 0)
		return (0);
	else if (power == 0)
		return (1);
	else if (power == 1)
		return (nb);
	i = 0;
	while (i < power)
	{
		res = res * nb;
		i++;
	}
	return (res);
}
