int	ft_sqrt(int nb)
{
	int	i;
	int	j;
	int	res;

	i = 1;
	j = 1;
	res = nb;
	while (res > 0)
	{
		res = res - j;
		j += 2;
		i++;
	}
	if (res == 0)
		return (i - 1);
	return (0);
}
