void	ft_ultimate_div_mod(int *a, int *b)
{
	int	mod;
	int	div;

	if (*b == 0)
	{
		div = 0;
		mod = 0;
	}
	else
	{
		mod = *a % *b;
		div = *a / *b;
	}
	*a = div;
	*b = mod;
}
